#include "libft.h"

int	ft_intlen(long int n)
{
	long int	nb;
	int			i;

	if (!n)
		return (1);
	nb = n;
	i = 0;
	if (nb < 0)
	{
		nb = -nb;
		i++;
	}
	while (nb > 0)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
